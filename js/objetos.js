function movilidad(tipo, ciclo, pais, ciudad)
{
    this.tipo = tipo;
    this.ciclo = ciclo;
    this.pais = pais;
    this.ciudad = ciudad;
}

function movilidadLocalizacion(tipo, ciclo, pais, ciudad, latitud, longitud)
{
    this.tipo = tipo;
    this.ciclo = ciclo;
    this.pais = pais;
    this.ciudad = ciudad;
    this.latitud = latitud;
    this.longitud = longitud;
}